public class ong {

    private String nomeTemporario;
    private Integer idade;
    private String cor;
    private String sexo;
    private Boolean castrado;
    private Boolean vacinado;
    private Boolean vermifugado;
    private Boolean saudavel;

    public String getNomeTemporario() {
        return nomeTemporario;
    }

    public void setNomeTemporario(String nomeTemporario) {
        this.nomeTemporario = nomeTemporario;
    }

    public Integer getIdade() {
        return idade;
    }

    public void setIdade(Integer idade) {
        this.idade = idade;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public Boolean getCastrado() {
        return castrado;
    }

    public void setCastrado(Boolean castrado) {
        this.castrado = castrado;
    }

    public Boolean getVacinado() {
        return vacinado;
    }

    public void setVacinado(Boolean vacinado) {
        this.vacinado = vacinado;
    }

    public Boolean getVermifugado() {
        return vermifugado;
    }

    public void setVermifugado(Boolean vermifugado) {
        this.vermifugado = vermifugado;
    }

    public Boolean getSaudavel() {
        return saudavel;
    }

    public void setSaudavel(Boolean saudavel) {
        this.saudavel = saudavel;
    }

}
